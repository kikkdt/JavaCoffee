    private void tblProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductsMouseClicked
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
            evt.consume();
            int indexRow = tblProducts.getSelectedRow();
            DialogAmount dlg = new DialogAmount(null, true);
            dlg.txtAmount.setText(dtm.getValueAt(indexRow, 1) + "");
            dlg.btnSave.addActionListener((ActionEvent e) -> {
                int soLuong = Integer.parseInt(dlg.txtAmount.getText());
                String tenSP = dtm.getValueAt(indexRow, 0).toString();
                int maSP = sp.getMaSP(tenSP);
                int maBan = Integer.parseInt(tableSelected.getActionCommand());
                if (soLuong > 0) {
                    if (new CTHoaDon_BUS().update(thanhtoan.getHoadon().getMaHD(), maSP, soLuong) > 0) {
                        dlg.btnSave.setActionCommand("performed");
                        dlg.dispose();
                        loadBillServed(maBan);
                    } else {
                        JOptionPane.showMessageDialog(null, "Lưu không thành công.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                    }
                } else {
                    if (new CTHoaDon_BUS().delete(thanhtoan.getHoadon().getMaHD(), maSP) > 0) {
                        dlg.btnSave.setActionCommand("performed");
                        JOptionPane.showMessageDialog(null, "Xoá thành công.", "Chúc mừng", JOptionPane.INFORMATION_MESSAGE);
                        dlg.dispose();
                        loadBillServed(maBan);
                    } else {
                        JOptionPane.showMessageDialog(null, "Xoá không thành công.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                    }
                }
            });
            dlg.getRootPane().setDefaultButton(dlg.btnSave);
            dlg.setLocationRelativeTo(null);
            dlg.setVisible(true);
        }
    }//GEN-LAST:event_tblProductsMouseClicked

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        int maHD = thanhtoan.getHoadon().getMaHD();
        String tenKH = kh.getTenKH(thanhtoan.getHoadon().getMaKH());
        int diemTichLuy = kh.getDiemTichLuy(thanhtoan.getHoadon().getMaKH());
        String tenNV = nv.getTenNhanVien(thanhtoan.getHoadon().getMaNV());
        Timestamp ngayXuatHD = thanhtoan.getHoadon().getNgayXuatHD();
        String tenBan = thanhtoan.getBan().getTenBan();
        double tongTien = thanhtoan.getHoadon().getTongTien();
        double giamGia = thanhtoan.getHoadon().getGiamGia();
        double thanhTien = thanhtoan.getHoadon().getThanhTien();

        //Dialog payment
        DialogPayment payment = new DialogPayment(null, true, thanhTien);
        payment.btnThanhToan.addActionListener((e) -> {
            if (payment.txtTienDua.getText().isBlank() || payment.getTienTra() < 0) {
                JOptionPane.showMessageDialog(null, "Tiền khách thanh toán không đúng.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                return;
            }

            String tienDua = String.format("%,.0f VNĐ", Double.parseDouble(payment.txtTienDua.getText()));
            String tienTra = payment.txtTienTra.getText();

            if (new ThanhToan_BUS().completeThanhToan(thanhtoan.getHoadon().getMaHD(), thanhtoan.getBan().getMaBan()) > 0) {
                payment.dispose();

                //Convert ArrayList to JRBeanCollectionDataSource
                JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(xuatCTHD.getList(maHD));

                //Map to hold Jasper report parametes
                Map<String, Object> parameters = new HashMap<>();
                parameters.put("maHD", maHD);
                parameters.put("tenKH", tenKH);
                parameters.put("diemTichLuy", diemTichLuy);
                parameters.put("tenNV", tenNV);
                parameters.put("ngayXuatHD", ngayXuatHD);
                parameters.put("tenBan", tenBan);
                parameters.put("tongTien", tongTien);
                parameters.put("giamGia", giamGia);
                parameters.put("thanhTien", thanhTien);
                parameters.put("tienDua", tienDua);
                parameters.put("tienTra", tienTra);
                parameters.put("CollectionBeanParam", dataSource);

                try {
                    //Read jrxml file and creating JasperDesign object
                    InputStream input = new FileInputStream(new File("src/javacoffee/view/report/reportHoaDon.jrxml"));
                    JasperDesign jasperDesign = JRXmlLoader.load(input);

                    //Compiling jrxml file
                    JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);

                    //Generate PDF
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameters, new JREmptyDataSource());

                    //Display PDF file
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);
                    jasperViewer.setTitle("Java Coffee");
                    jasperViewer.setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/logo-icon.png")));
                    jasperViewer.setVisible(true);

                } catch (FileNotFoundException | JRException ex) {
                    Logger.getLogger(TinhLuongGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "THANH TOÁN THẤT BẠI.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            }
        });

        payment.setLocationRelativeTo(null);
        payment.setVisible(true);
        int indexCurrent = pnlBan.getSelectedIndex();
        pnlBan.removeAll();
        loadPanelBan();
        pnlBan.setSelectedIndex(indexCurrent);
        loadPnlCenter(true);
        loadPnlRight(false);
    }//GEN-LAST:event_btnThanhToanActionPerformed

