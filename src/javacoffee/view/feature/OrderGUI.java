    private void loadPanelBan() {
        ArrayList<KhuVuc_pojo> lstKhuVuc = new KhuVuc_BUS().getList();
        if (lstKhuVuc.isEmpty()) {
            return;
        }
        int i = 0;
        for (KhuVuc_pojo kv : lstKhuVuc) {
            JLabel lblTabKhuVuc = new JLabel(kv.getTenKhuVuc());
            lblTabKhuVuc.setPreferredSize(new Dimension(70, 30));
            lblTabKhuVuc.setFont(new Font("Segoe UI", Font.BOLD, 14));

            ArrayList<Ban_pojo> lstBan = new Ban_BUS().getList(kv.getMaKhuVuc());
            if (lstBan.isEmpty()) {
                return;
            }
            JPanel pnlListBan = new JPanel();
            pnlListBan.setPreferredSize(pnlBan.getPreferredSize());
            pnlListBan.setMaximumSize(pnlBan.getMaximumSize());
            pnlListBan.setMinimumSize(pnlBan.getMinimumSize());
            pnlListBan.setLayout(new FlowLayout(FlowLayout.LEFT));

            lstBan.stream().map(ban -> {
                JButton btnBan = new JButton(ban.getTenBan());
                btnBan.setBorder(null);
                btnBan.setBackground(ban.isTrangThai() == true ? new Color(226, 76, 75) : new Color(49, 186, 124));
                btnBan.setForeground(Color.white);
                btnBan.setFont(new Font("Segoe UI", Font.BOLD, 17));
                btnBan.setHorizontalAlignment(0);
                Icon icon = new ImageIcon(getClass().getResource("/table.png"));
                btnBan.setIcon(icon);
                btnBan.setHorizontalTextPosition(0);
                btnBan.setVerticalTextPosition(3);
                btnBan.setActionCommand(ban.getMaBan() + "");
                //Add event for each table
                btnBan.addActionListener((ActionEvent e) -> {
                    tableSelected = btnBan;
                    if (ban.isTrangThai()) {
                        loadBillServed(ban.getMaBan());
                        loadPnlRight(true);
                    } else {
                        thanhtoan = null;
                        lblTeNBan.setText(ban.getTenBan());
                        lblKhuVuc.setText(kv.getTenKhuVuc());
                        lblTrangThai.setText("Bàn trống");
                        loadPnlCenter(true);
                        loadPnlRight(false);
                    }
                });
                return btnBan;
            }).forEachOrdered(btnBan -> {
                pnlListBan.add(btnBan);
            });
            pnlBan.addTab("", pnlListBan);
            pnlBan.setTabComponentAt(i++, lblTabKhuVuc);
        }
    }

    private void loadBillServed(int maBan) {
        //Get bill info is "unpaid" with table number selected
        thanhtoan = new ThanhToan_BUS().getThanhToan(maBan);
        if (thanhtoan == null) {
            return;
        }

        lblTeNBan1.setText(thanhtoan.getBan().getTenBan());
        lblTrangThai1.setText(thanhtoan.getBan().isTrangThai() == true ? "Đang phục vụ" : "Bàn trống");
        lblKhuVuc1.setText(thanhtoan.getKhuvuc().getTenKhuVuc());
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        lblGioDen.setText(sdf.format(thanhtoan.getHoadon().getNgayXuatHD()));
        String tenKH = kh.getTenKH(thanhtoan.getHoadon().getMaKH());
        txtKhachHang.setText(tenKH);
        lblTongTien.setText(String.format("%,.0f", thanhtoan.getHoadon().getTongTien()));
        lblGiamGia.setText(String.format("%,.0f", thanhtoan.getHoadon().getGiamGia()));
        lblThanhTien.setText(String.format("%,.0f", thanhtoan.getHoadon().getThanhTien()));

        tblProducts.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        tblProducts.getTableHeader().setReorderingAllowed(false);
        tblProducts.setRowHeight(24);
        tblProducts.setFont(new Font("Segoe UI", Font.PLAIN, 14));
        tblProducts.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        //Set DefaultTableModel & add header table
        tblProducts.setModel(new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        });
        dtm = (DefaultTableModel) tblProducts.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        dtm.addColumn("Tên");
        dtm.addColumn("Số lượng");
        dtm.addColumn("Đơn giá");
        dtm.addColumn("Thành tiền");

        //Setting column width & turn off resizable
        tblProducts.getColumnModel().getColumn(0).setPreferredWidth(140);
        tblProducts.getColumnModel().getColumn(1).setPreferredWidth(60);
        for (int i = 0; i < 4; i++) {
            tblProducts.getColumnModel().getColumn(i).setResizable(false);
        }
        tblProducts.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        thanhtoan.getCtHoaDon().forEach(cthd -> {
            dtm.addRow(new Object[]{
                sp.getTenSP(cthd.getMaSP()),
                cthd.getSoLuong(),
                String.format("%,.0f", sp.getGiaBan(cthd.getMaSP())),
                String.format("%,.0f", cthd.getThanhTien())
            });
        });
        pnlCenter.removeAll();
        pnlCenter.add(pnlBillServed);
        pnlCenter.repaint();
        pnlCenter.revalidate();
    }

    private void loadMenu() {
        ArrayList<DanhMuc_pojo> lstDanhMuc = new DanhMuc_BUS().getList();
        if (lstDanhMuc.isEmpty()) {
            return;
        }
        for (DanhMuc_pojo dm : lstDanhMuc) {
            JLabel lblTabDanhMuc = new JLabel(dm.getTenDanhMuc());
            lblTabDanhMuc.setPreferredSize(new Dimension(80, 70));
            lblTabDanhMuc.setFont(new Font("Segoe UI", Font.BOLD, 14));

            ArrayList<SanPham_pojo> lstSanPham = new SanPham_BUS().getList(dm.getMaDanhMuc());
            if (lstSanPham.isEmpty()) {
                return;
            }
            JPanel pnlListMenu = new JPanel();
//            pnlListMenu.setMaximumSize(pnlMenu.getMaximumSize());
//            pnlListMenu.setMinimumSize(pnlMenu.getMinimumSize());
            pnlListMenu.setPreferredSize(new Dimension(230, 500));
            pnlListMenu.setLayout(new FlowLayout(FlowLayout.LEFT, 5, 5));

            lstSanPham.stream().map(itemSP -> {
                JButton btnMenu = new JButton(itemSP.getTenSanPham());
                btnMenu.setBorder(null);
                btnMenu.setFont(new Font("Segoe UI", Font.PLAIN, 17));
                btnMenu.setHorizontalAlignment(0);
                btnMenu.setToolTipText(itemSP.getTenSanPham());
                ImageIcon icon;
                if (itemSP.getHinhAnh() == null) {
                    icon = new ImageIcon(getClass().getResource("/no-data.png"));
                } else {
                    icon = new ImageIcon(getClass().getResource("/" + itemSP.getHinhAnh()));
                    Image image = icon.getImage();
                    Image newImage = image.getScaledInstance(180, 120, java.awt.Image.SCALE_SMOOTH);
                    icon = new ImageIcon(newImage);
                }
                btnMenu.setIcon(icon);
                btnMenu.setHorizontalTextPosition(SwingConstants.CENTER);
                btnMenu.setVerticalTextPosition(SwingConstants.BOTTOM);
                btnMenu.setActionCommand(itemSP.getMaSanPham() + "");
                return btnMenu;
            }).map(btnMenu -> {
                btnMenu.addMouseListener(new MouseListener() {
                    @Override
                    public void mouseClicked(MouseEvent e) {
                        if (e.getClickCount() == 2 && !e.isConsumed()) {
                            e.consume();
                            if (new CTHoaDon_BUS().insert(thanhtoan.getHoadon().getMaHD(), Integer.parseInt(btnMenu.getActionCommand())) > 0) {
                                loadBillServed(Integer.parseInt(tableSelected.getActionCommand()));
                            } else {
                                JOptionPane.showMessageDialog(null, "Order thất bại :(", "Lỗi gọi món", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                            }
                        }
                    }

                    @Override
                    public void mousePressed(MouseEvent e) {
                    }

                    @Override
                    public void mouseReleased(MouseEvent e) {
                    }

                    @Override
                    public void mouseEntered(MouseEvent e) {
                    }

                    @Override
                    public void mouseExited(MouseEvent e) {
                    }
                });
                return btnMenu;
            }).forEachOrdered(btnMenu -> {
                pnlListMenu.add(btnMenu);
            });
            tabPnlMenu.addTab("", pnlListMenu);
            tabPnlMenu.setTabComponentAt(dm.getMaDanhMuc() - 1, lblTabDanhMuc);
            tabPnlMenu.setToolTipTextAt(dm.getMaDanhMuc() - 1, lblTabDanhMuc.getText());
        }
    }

