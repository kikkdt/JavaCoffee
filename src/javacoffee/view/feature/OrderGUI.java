    private void loadPnlRight(boolean displayMenu) {
        pnlRight.removeAll();
        if (displayMenu) {
            if (tabPnlMenu.getTabCount() == 0) {
                loadMenu();
            }
            pnlRight.add(tabPnlMenu);
        } else {
            pnlRight.add(bgrMenu);
        }
        pnlRight.repaint();
        pnlRight.revalidate();
    }

