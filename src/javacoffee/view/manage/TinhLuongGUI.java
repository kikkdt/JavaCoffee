    private void btnXuatFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatFileActionPerformed
        if (lstLuong.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lỗi, danh sách lương hiện đang trống.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            return;
        }

        //Convert ArrayList to JRBeanCollectionDataSource
        JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(lstLuong);

        //Map to hold Jasper report parametes
        Map<String, Object> parameters = new HashMap<>();
        parameters.put("luongCB", luongCB);
        parameters.put("CollectionBeanParam", dataSource);

        try {
            //Read jrxml file and creating JasperDesign object
            InputStream input = new FileInputStream(new File("src/javacoffee/view/report/reportLuong.jrxml"));
            JasperDesign jasperDesign = JRXmlLoader.load(input);

            //Compiling jrxml file
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);

            //Generate PDF
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameters, new JREmptyDataSource());

            //Display PDF file
            JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);
            jasperViewer.setTitle("Java Coffee");
            jasperViewer.setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/logo-icon.png")));
            jasperViewer.setVisible(true);

        } catch (FileNotFoundException | JRException ex) {
            Logger.getLogger(TinhLuongGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnXuatFileActionPerformed

    private void btnTinhLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhLuongActionPerformed
        Timestamp from, to;
        try {
            from = new Timestamp(sdf.parse(sdf.format(dateFrom.getDate())).getTime());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu không đúng.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            return;
        }
        try {
            to = new Timestamp(sdf.parse(sdf.format(dateTo.getDate())).getTime());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ngày kết thúc không đúng.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            return;
        }

        loadTableTinhLuong(from, to);
        jPanel3.removeAll();
        jPanel3.add(jScrollPane1);
        jPanel3.repaint();
        jPanel3.revalidate();
    }//GEN-LAST:event_btnTinhLuongActionPerformed

