    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if ("performed".equalsIgnoreCase(btnSave.getActionCommand()) || txtLuongCB.getText().isBlank()) {
            dialogLuongCB.dispose();
        } else {
            int chosen = JOptionPane.showConfirmDialog(null, "Lương cơ bản chưa được lưu. Lưu?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            if (chosen == JOptionPane.NO_OPTION) {
                dialogLuongCB.dispose();
            } else if (chosen == JOptionPane.YES_OPTION) {
                btnSave.doClick();
                if ("performed".equalsIgnoreCase(btnSave.getActionCommand())) {
                    dialogLuongCB.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnCancelActionPerformed

    private void dialogLuongCBWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_dialogLuongCBWindowClosing
        if ("performed".equalsIgnoreCase(btnSave.getActionCommand()) || txtLuongCB.getText().isBlank()) {
            dialogLuongCB.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        } else {
            int chosen = JOptionPane.showConfirmDialog(null, "Lương cơ bản chưa được lưu. Lưu?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            switch (chosen) {
                case JOptionPane.NO_OPTION:
                    dialogLuongCB.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                    break;
                case JOptionPane.CANCEL_OPTION:
                    dialogLuongCB.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
                    break;
                case JOptionPane.YES_OPTION:
                    btnSave.doClick();
                    if ("performed".equalsIgnoreCase(btnSave.getActionCommand())) {
                        dialogLuongCB.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                    }
                    break;
                default:
                    break;
            }
        }
    }//GEN-LAST:event_dialogLuongCBWindowClosing

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtLuongCB.getText().matches("^[0-9]+$")) {
            if (Double.parseDouble(txtLuongCB.getText()) >= 0) {
                luongCB = Double.parseDouble(txtLuongCB.getText());
                btnSave.setActionCommand("performed");
                dialogLuongCB.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi, lương cơ bản không được âm.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            }
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi, lương cơ bản phải là số.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void loadTableTinhLuong(Timestamp from, Timestamp to) {
        lstLuong = new Luong_BUS().calcLuong(luongCB, from, to);
        if (lstLuong.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lỗi, danh sách lương hiện đang trống.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
            return;
        }

        tblLuong.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        tblLuong.getTableHeader().setReorderingAllowed(false);
        tblLuong.setRowHeight(24);
        tblLuong.setFont(new Font("Segoe UI", Font.PLAIN, 14));
        tblLuong.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        //Set DefaultTableModel & add header table
        tblLuong.setModel(new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        });

        dtm = (DefaultTableModel) tblLuong.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        dtm.addColumn("Mã Nhân viên");
        dtm.addColumn("Tên Nhân viên");
        dtm.addColumn("Tổng ca làm");
        dtm.addColumn("Thành tiền");

        //Setting column width & turn off resizable
        for (int i = 0; i < 4; i++) {
            tblLuong.getColumnModel().getColumn(i).setResizable(false);
        }
        tblLuong.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);

        lstLuong.forEach((Luong_pojo luong) -> {
            dtm.addRow(new Object[]{
                luong.getMaNV(),
                luong.getTenNV(),
                luong.getTongCaLam(),
                String.format("%,.0f", luong.getThanhTien())
            });
        });
    }

