    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        lblCaLam.setText("Thêm Ca làm");
        txtTenCaLam.setText("");
        txtGioBatDau.setText("");
        txtGioKetThuc.setText("");

        if (btnSave.getActionListeners().length != 0) {
            for (ActionListener item : btnSave.getActionListeners()) {
                btnSave.removeActionListener(item);
            }
        }
        btnSave.addActionListener((ActionEvent e) -> {
            String tenCaLam = txtTenCaLam.getText().isBlank() ? "" : txtTenCaLam.getText();
            String regexStr = "^([0-1]?[0-9]|2[0-3]):[0-5][0-9]$";
            Time gioBatDau = null, gioKetThuc = null;
            if (txtGioBatDau.getText().matches(regexStr) && txtGioKetThuc.getText().matches(regexStr)) {
                try {
                    gioBatDau = new Time(sdf.parse(txtGioBatDau.getText()).getTime());
                    gioKetThuc = new Time(sdf.parse(txtGioKetThuc.getText()).getTime());
                } catch (ParseException ex) {
                    //Logger.getLogger(CaLamGUI.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Giờ bắt đầu và giờ kết thúc phải là định dạng 24h.\nVí dụ: 07:00 hoặc 22:00", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Giờ bắt đầu và giờ kết thúc phải là định dạng 24h.\nVí dụ: 07:00 hoặc 22:00", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                return;
            }

            if (new CaLam_BUS().insert(tenCaLam, gioBatDau, gioKetThuc) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm ca làm thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                dialogCaLam.dispose();
                btnSave.setActionCommand("performed");
                loadTableCaLam();
            }
        }
        );
        dialogCaLam.getRootPane().setDefaultButton(btnSave);
        dialogCaLam.setLocationRelativeTo(null);
        dialogCaLam.setModal(true);
        dialogCaLam.setVisible(true);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (tblCaLam.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn 1 ca làm để sửa thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        } else {
            int index = tblCaLam.getSelectedRow();
            dtm = (DefaultTableModel) tblCaLam.getModel();
            lblCaLam.setText("Cập nhật Ca làm");
            txtTenCaLam.setText(dtm.getValueAt(index, 1).toString());
            txtGioBatDau.setText(dtm.getValueAt(index, 2).toString());
            txtGioKetThuc.setText(dtm.getValueAt(index, 3).toString());

            if (btnSave.getActionListeners().length != 0) {
                for (ActionListener item : btnSave.getActionListeners()) {
                    btnSave.removeActionListener(item);
                }
            }
            btnSave.addActionListener((ActionEvent e) -> {
                int maCaLam = Integer.parseInt(dtm.getValueAt(index, 0).toString());
                String tenCaLam = txtTenCaLam.getText().isBlank() ? "" : txtTenCaLam.getText();
                String regexStr = "^([0-1]?[0-9]|2[0-3]):[0-5][0-9]$";
                Time gioBatDau = null, gioKetThuc = null;
                if (txtGioBatDau.getText().matches(regexStr) && txtGioKetThuc.getText().matches(regexStr)) {
                    try {
                        gioBatDau = new Time(sdf.parse(txtGioBatDau.getText()).getTime());
                        gioKetThuc = new Time(sdf.parse(txtGioKetThuc.getText()).getTime());
                    } catch (ParseException ex) {
                        //Logger.getLogger(CaLamGUI.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(null, "Giờ bắt đầu và giờ kết thúc phải là định dạng 24h.\nVí dụ: 07:00 hoặc 22:00", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Giờ bắt đầu và giờ kết thúc phải là định dạng 24h.\nVí dụ: 07:00 hoặc 22:00", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
                    return;
                }
                if (new CaLam_BUS().update(maCaLam, tenCaLam, gioBatDau, gioKetThuc) > 0) {
                    JOptionPane.showMessageDialog(null, "Cập nhật ca làm thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    dialogCaLam.dispose();
                    btnSave.setActionCommand("performed");
                    loadTableCaLam();
                }
            });
            dialogCaLam.getRootPane().setDefaultButton(btnSave);
            dialogCaLam.setLocationRelativeTo(null);
            dialogCaLam.setModal(true);
            dialogCaLam.setVisible(true);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int[] rowSelected = tblCaLam.getSelectedRows();
        if (rowSelected.length == 0)
            JOptionPane.showMessageDialog(null, "Vui lòng chọn ít nhất 1 ca làm để xóa.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        else {
            dtm = (DefaultTableModel) tblCaLam.getModel();
            int chosen = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xoá " + rowSelected.length + " ca làm không?", "Thông báo", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION);
            if (chosen == JOptionPane.YES_OPTION) {
                CaLam_BUS calam = new CaLam_BUS();
                int rowAffected = 0;
                for (int i = 0; i < rowSelected.length; i++) {
                    if (calam.delete(Integer.parseInt(dtm.getValueAt(rowSelected[i], 0).toString())) > 0) {
                        rowAffected++;
                    }
                }
                if (rowAffected == rowSelected.length) {
                    JOptionPane.showMessageDialog(null, "Xoá thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                loadTableCaLam();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String strSearch = "^.*" + txtSearch.getText().toLowerCase() + ".*$";
        dtm = (DefaultTableModel) tblCaLam.getModel();
        dtm.setRowCount(0);
        lstCaLam.stream().filter(calam -> (String.valueOf(calam.getMaCaLam()).matches(strSearch) || calam.getTenCaLam().toLowerCase().matches(strSearch) || calam.getGioBatDau().toString().toLowerCase().matches(strSearch) || calam.getGioKetThuc().toString().toLowerCase().matches(strSearch))).forEachOrdered(calam -> {
            dtm.addRow(new Object[]{
                calam.getMaCaLam(),
                calam.getTenCaLam(),
                calam.getGioBatDau(),
                calam.getGioKetThuc()
            });
        });
    }//GEN-LAST:event_txtSearchKeyReleased

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if ("performed".equalsIgnoreCase(btnSave.getActionCommand()) || (txtTenCaLam.getText().isBlank() && txtGioBatDau.getText().isBlank() && txtGioKetThuc.getText().isBlank())) {
            dialogCaLam.dispose();
        } else {
            int chosen = JOptionPane.showConfirmDialog(null, "Thông tin nhân viên chưa được lưu. Bạn có muốn lưu không?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            if (chosen == JOptionPane.NO_OPTION) {
                dialogCaLam.dispose();
            } else if (chosen == JOptionPane.YES_OPTION) {
                btnSave.doClick();
                if ("performed".equalsIgnoreCase(btnSave.getActionCommand())) {
                    dialogCaLam.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnCancelActionPerformed

    private void dialogCaLamWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_dialogCaLamWindowClosing
        if ("performed".equalsIgnoreCase(btnSave.getActionCommand()) || (txtTenCaLam.getText().isBlank() && txtGioBatDau.getText().isBlank() && txtGioKetThuc.getText().isBlank())) {
            dialogCaLam.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        } else {
            int chosen = JOptionPane.showConfirmDialog(null, "Thông tin nhân viên chưa được lưu. Bạn có muốn lưu không?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            switch (chosen) {
                case JOptionPane.NO_OPTION:
                    dialogCaLam.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                    break;
                case JOptionPane.CANCEL_OPTION:
                    dialogCaLam.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
                    break;
                case JOptionPane.YES_OPTION:
                    btnSave.doClick();
                    if ("performed".equals(btnSave.getActionCommand())) {
                        dialogCaLam.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                    }
                    break;
                default:
                    break;
            }
        }
    }//GEN-LAST:event_dialogCaLamWindowClosing

    private void loadTableCaLam() {
        lstCaLam = new CaLam_BUS().getList();
        if (lstCaLam.isEmpty()) {
            return;
        }

        tblCaLam.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        tblCaLam.getTableHeader().setReorderingAllowed(false);
        tblCaLam.setRowHeight(24);
        tblCaLam.setFont(new Font("Segoe UI", Font.PLAIN, 14));
        tblCaLam.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);

        //Set DefaultTableModel & add header table
        tblCaLam.setModel(new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        });
        tblCaLam.getModel().addTableModelListener((TableModelEvent e) -> {
            txtTongSo.setText(tblCaLam.getRowCount() + "");
        });

        dtm = (DefaultTableModel) tblCaLam.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        dtm.addColumn("Mã Ca làm");
        dtm.addColumn("Tên Ca làm");
        dtm.addColumn("Giờ bắt đầu");
        dtm.addColumn("Giờ kết thúc");

        //Setting column width & turn off resizable
        for (int i = 0; i < 4; i++) {
            tblCaLam.getColumnModel().getColumn(i).setResizable(false);
        }
        tblCaLam.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        lstCaLam.forEach(calam -> {
            dtm.addRow(new Object[]{
                calam.getMaCaLam(),
                calam.getTenCaLam(),
                sdf.format(calam.getGioBatDau()),
                sdf.format(calam.getGioKetThuc())
            });
        });
    }


