    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int[] rowSelected = tblTaiKhoan.getSelectedRows();
        if (rowSelected.length == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn ít nhất 1 bàn để xóa.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        } else {
            dtm = (DefaultTableModel) tblTaiKhoan.getModel();
            int chosen = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xoá " + rowSelected.length + " tài khoản không?", "Thông báo", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION);
            if (chosen == JOptionPane.YES_OPTION) {
                int rowAffected = 0;
                for (int i = 0; i < rowSelected.length; i++) {
                    if (new TaiKhoan_BUS().delete(dtm.getValueAt(rowSelected[i], 1).toString()) > 0) {
                        rowAffected++;
                    }
                }
                if (rowAffected == rowSelected.length) {
                    JOptionPane.showMessageDialog(null, "Xoá thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                loadTableTaiKhoan();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void txtSearchTaiKhoanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchTaiKhoanKeyReleased
        String strSearch = "^.*" + txtSearchTaiKhoan.getText().toLowerCase() + ".*$";
        dtm = (DefaultTableModel) tblTaiKhoan.getModel();
        dtm.setRowCount(0);
        int i = 0;
        for (TaiKhoan_pojo tk : lstTaiKhoan) {
            if (tk.getTenDangNhap().matches(strSearch) || tk.getTenHienThi().toLowerCase().matches(strSearch) || loaiTK.getTenLoaiTK(tk.getIdLoaiTK()).toLowerCase().matches(strSearch) || String.valueOf(tk.getMaNV()).matches(strSearch)) {
                dtm.addRow(new Object[]{
                    ++i,
                    tk.getTenDangNhap(),
                    tk.getMatKhau(),
                    tk.getTenHienThi(),
                    loaiTK.getTenLoaiTK(tk.getIdLoaiTK()),
                    tk.getMaNV() + "-" + nhanvien.getTenNhanVien(tk.getMaNV())
                });
            }
        }
    }//GEN-LAST:event_txtSearchTaiKhoanKeyReleased

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if (txtTenDangNhap.getText().isBlank() && txtMatKhau.getPassword().length == 0 && txtMatKhau2.getPassword().length == 0)
            dialogTaiKhoan.dispose();
        else {
            if ("performed".equals(btnSave.getActionCommand())) {
                dialogTaiKhoan.dispose();
            } else {
                int chosen = JOptionPane.showConfirmDialog(null, "Thông tin tài khoản chưa được lưu. Bạn có muốn lưu không?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
                if (chosen == JOptionPane.NO_OPTION) {
                    dialogTaiKhoan.dispose();
                } else if (chosen == JOptionPane.YES_OPTION) {
                    btnSave.doClick();
                    if ("performed".equals(btnSave.getActionCommand())) {
                        dialogTaiKhoan.dispose();
                    }
                }
            }
        }
    }//GEN-LAST:event_btnCancelActionPerformed

    private void dialogTaiKhoanformWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_dialogTaiKhoanformWindowClosing
        if (txtTenDangNhap.getText().isBlank() && txtMatKhau.getPassword().length == 0 && txtMatKhau2.getPassword().length == 0)
            dialogTaiKhoan.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        else {
            if ("performed".equals(btnSave.getActionCommand())) {
                dialogTaiKhoan.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            } else {
                int chosen = JOptionPane.showConfirmDialog(null, "Thông tin tài khoản chưa được lưu. Bạn có muốn lưu không?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
                switch (chosen) {
                    case JOptionPane.NO_OPTION:
                        dialogTaiKhoan.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                        break;
                    case JOptionPane.CANCEL_OPTION:
                        dialogTaiKhoan.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
                        break;
                    case JOptionPane.YES_OPTION:
                        btnSave.doClick();
                        if ("performed".equals(btnSave.getActionCommand())) {
                            dialogTaiKhoan.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                        }
                        break;
                    default:
                        break;
                }
            }
        }
    }//GEN-LAST:event_dialogTaiKhoanformWindowClosing
    
