    private void loadTableTaiKhoan() {
        tblTaiKhoan.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        tblTaiKhoan.getTableHeader().setReorderingAllowed(false);
        tblTaiKhoan.setRowHeight(24);
        tblTaiKhoan.setFont(new Font("Segoe UI", Font.PLAIN, 14));
        tblTaiKhoan.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);

        //Set DefaultTableModel & add header table
        tblTaiKhoan.setModel(new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        });
        tblTaiKhoan.getModel().addTableModelListener((e) -> {
            txtTongSo.setText(tblTaiKhoan.getRowCount() + "");
        });
        
        dtm = (DefaultTableModel) tblTaiKhoan.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        dtm.addColumn("STT");
        dtm.addColumn("Tên đăng nhập");
        dtm.addColumn("Mật khẩu");
        dtm.addColumn("Tên hiển thị");
        dtm.addColumn("Loại tài khoản");
        dtm.addColumn("Nhân viên");

        //Setting column width & turn off resizable
        tblTaiKhoan.getColumnModel().getColumn(0).setMaxWidth(50);
        for (int i = 0; i < 6; i++) {
            tblTaiKhoan.getColumnModel().getColumn(i).setResizable(false);
        }
        tblTaiKhoan.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        lstTaiKhoan = new TaiKhoan_BUS().getList();
        if (lstTaiKhoan.isEmpty()) {
            return;
        }
        int i = 0;
        for (TaiKhoan_pojo tk : lstTaiKhoan) {
            dtm.addRow(new Object[]{
                ++i,
                tk.getTenDangNhap(),
                tk.getMatKhau(),
                tk.getTenHienThi(),
                loaiTK.getTenLoaiTK(tk.getIdLoaiTK()),
                tk.getMaNV() + "-" + nhanvien.getTenNhanVien(tk.getMaNV())
            });
        }
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        txtTenDangNhap.setText("");
        txtMatKhau.setText("");
        txtMatKhau2.setText("");
        txtNhanVien.setText("");
        loadCbLoaiTK();
        if (btnSave.getActionListeners().length != 0) {
            for (ActionListener item : btnSave.getActionListeners()) {
                btnSave.removeActionListener(item);
            }
        }
        btnSave.addActionListener((ActionEvent e) -> {
            int maNV = 0;
            String tenHienThi = "", matKhau = "", matKhau2 = "";
            if (!txtNhanVien.getText().isBlank() && txtMatKhau.getPassword().length != 0 && txtMatKhau2.getPassword().length != 0) {
                String[] arrNV = txtNhanVien.getText().split("-");
                maNV = Integer.parseInt(arrNV[0]);
                tenHienThi = arrNV[1];
                matKhau = Arrays.toString(txtMatKhau.getPassword());
                matKhau2 = Arrays.toString(txtMatKhau2.getPassword());
            }
            if (new TaiKhoan_BUS().insert(txtTenDangNhap.getText(), matKhau, matKhau2, tenHienThi, loaiTK.getIdLoaiTK(cbLoaiTK.getSelectedItem().toString()), maNV) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm tài khoản thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                dialogTaiKhoan.dispose();
                btnSave.setActionCommand("performed");
                loadTableTaiKhoan();
            }
        });
        
        dialogTaiKhoan.setModal(true);
        dialogTaiKhoan.setLocationRelativeTo(null);
        dialogTaiKhoan.setVisible(true);
    }//GEN-LAST:event_btnThemActionPerformed

