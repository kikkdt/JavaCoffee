    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        jfc = new JFileChooser(FileSystemView.getFileSystemView()) {
            @Override
            public void approveSelection() {
                super.approveSelection();
                File f = jfc.getSelectedFile();
                String path = f.getAbsolutePath() + ".bak";
                txtSaoLuu.setText(path);
            }
        };
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Backup Files (*.bak)", "bak");
        jfc.setDialogTitle("Sao lưu");
        jfc.setFileFilter(filter);
        jfc.showSaveDialog(this);
    }//GEN-LAST:event_btnBrowseActionPerformed

    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        jfc = new JFileChooser(FileSystemView.getFileSystemView()) {
            @Override
            public void approveSelection() {
                super.approveSelection();
                File f = jfc.getSelectedFile();
                if (f.exists()) {
                    String path = f.getAbsolutePath();
                    txtPhucHoi.setText(path);
                }
            }
        };

        FileNameExtensionFilter filter = new FileNameExtensionFilter("Backup Files (*.bak)", "bak");
        jfc.setDialogTitle("Phục hồi");
        jfc.setFileFilter(filter);
        jfc.showOpenDialog(this);
    }//GEN-LAST:event_btnUploadActionPerformed

    private void btnSaoLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaoLuuActionPerformed
        try {
            String strSqlBackup = "backup database dbJavaCoffee to disk = '" + txtSaoLuu.getText() + "' with init";
            Statement statement = sql.open().createStatement();
            statement.execute(strSqlBackup);
            JOptionPane.showMessageDialog(null, "Sao lưu dữ liệu thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Sao lưu dữ liệu thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        } finally {
            sql.close();
        }
    }//GEN-LAST:event_btnSaoLuuActionPerformed

    private void btnPhucHoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPhucHoiActionPerformed
        try {
            String strSqlRestore = "alter database dbJavaCoffee set offline with rollback immediate\n"
                    + "restore database dbJavaCoffee from disk='" + txtPhucHoi.getText() + "' with replace\n"
                    + "alter database dbJavaCoffee set online with rollback immediate";

            Statement statement = sql.open().createStatement();
            statement.execute(strSqlRestore);
            JOptionPane.showMessageDialog(null, "Phục hồi dữ liệu thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        } catch (HeadlessException | SQLException e) {
            Logger.getLogger(SaoLuuGUI.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Phục hồi dữ liệu thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE, new ImageIcon(getClass().getResource("/error.png")));
        } finally {
            sql.close();
        }
    }//GEN-LAST:event_btnPhucHoiActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            CallableStatement callableStatement = sql.open().prepareCall("{call XOA}");
            
            callableStatement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Xóa dữ liệu thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            Logger.getLogger(SaoLuuGUI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Xóa dữ liệu thất bại.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } finally {
            sql.close();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

